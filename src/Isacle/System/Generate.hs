allEntries :: SysDoc -> [(BusSection, PeriphEntry)]
allEntries doc = [(bs, pe) | bs <- sdBuses doc, pe <- bsEntries bs]

-- ---------------------------------------------------------------------------
-- sysExtractMemoryMap
-- ---------------------------------------------------------------------------

-- | Human-readable memory map table derived from a 'SysDoc'.
--
-- Example:
--
-- > MEMORY MAP
-- > ==========
-- > Bus       Name              Base         End          Size
-- > ---------------------------------------------------------------
-- > databus   uart0             0x00000100   0x00000102   0x00000003
-- > databus   gpio0             0x00000300   0x00000301   0x00000002
-- >
-- > CPU: cpu0 (AVR8)
-- >
-- > REGISTERS
-- > =========
-- > uart0 (databus @ 0x00000100)
-- >   +0x00  RW  UDR   TX write / RX read
-- >   +0x01  RO  USR   Status
-- >   +0x02  RW  UBRR  Baud rate divisor
sysExtractMemoryMap :: SysDoc -> String
sysExtractMemoryMap doc = unlines $ concat
    [ mapHeader
    , sortBy (comparing (\(_,pe) -> peBase pe)) (allEntries doc)
      >>= \(bs, pe) -> [renderRow bs pe]
    , cpuSection
    , regSection
    ]
  where
    col = 13

    mapHeader =
        [ "MEMORY MAP"
        , "=========="
        , padRight 10 "Bus" ++ padRight 18 "Name"
          ++ padRight col "Base" ++ padRight col "End" ++ "Size"
        , replicate 65 '-'
        ]

    sz pe   = specSize (peSpec pe)
    base pe = peBase pe

    renderRow bs pe =
        padRight 10 (bsName bs) ++ padRight 18 (peName pe)
        ++ padRight col (hex32 (base pe))
        ++ padRight col (hex32 (base pe + sz pe - 1))
        ++ hex32 (sz pe)

    cpuSection
        | null (sdCPUs doc) = []
        | otherwise = ["", "CPU: " ++ intercalate ", " (sdCPUs doc)]

    regSection
        | null (allEntries doc) = []
        | otherwise = "" : "REGISTERS" : "=========" : concatMap renderPeriph (allEntries doc)

    renderPeriph (bs, pe) =
        (peName pe ++ " (" ++ bsName bs ++ " @ " ++ hex32 (peBase pe) ++ ")")
        : map renderField (psFields (peSpec pe))
        ++ [""]

    renderField f =
        "  +" ++ hex8 (fieldOffset f) ++ "  "
        ++ padRight 4 (renderAccess (fieldAccess f)) ++ "  "
        ++ padRight 8 (fieldName f) ++ "  "
        ++ fieldDesc f

    renderAccess ReadOnly  = "RO"
    renderAccess ReadWrite = "RW"
    renderAccess WriteOnly = "WO"

    hex8 :: Word8 -> String
    hex8 b = "0x" ++ (if b < 16 then "0" else "") ++ showHex b ""

-- ---------------------------------------------------------------------------
-- sysGenCHeader
-- ---------------------------------------------------------------------------

-- | Generate a C header from a 'SysDoc'.
--
-- Produces @NAME_BASE@ and @NAME_SIZE@ macros for every peripheral,
-- plus per-register @NAME_REG@ offset macros.
--
-- Example (@guardName = "memmap"@):
--
-- > /* Auto-generated by ISACLE */
-- > #ifndef MEMMAP_H
-- > #define MEMMAP_H
-- >
-- > /* databus */
-- > #define UART0_BASE  0x100UL
-- > #define UART0_SIZE  0x3UL
-- > #define UART0_UDR   0x0UL
-- > #define UART0_USR   0x1UL
-- > #define UART0_UBRR  0x2UL
-- >
-- > #endif /* MEMMAP_H */
sysGenCHeader :: String -> SysDoc -> String
sysGenCHeader guardName doc = unlines $
    [ "/* Auto-generated by ISACLE */"
    , "#ifndef " ++ guard
    , "#define " ++ guard
    ]
    ++ concatMap renderBus (sdBuses doc)
    ++
    [ ""
    , "#endif /* " ++ guard ++ " */"
    ]
  where
    guard   = map toUpper guardName ++ "_H"
    up      = map toUpper
    hexUL w = "0x" ++ map toUpper (showHex w "") ++ "UL"

    renderBus bs =
        ("" : ("/* " ++ bsName bs ++ " */") : concatMap (renderEntry (bsName bs)) (bsEntries bs))

    renderEntry _busName pe =
        let prefix = up (peName pe)
            regs   = concatMap (renderReg prefix) (psFields (peSpec pe))
        in [ "#define " ++ prefix ++ "_BASE  " ++ hexUL (peBase pe)
           , "#define " ++ prefix ++ "_SIZE  " ++ hexUL (specSize (peSpec pe))
           ]
           ++ regs

    -- A register's offset macro (annotated with the C type the bits should be
    -- read as, from its 'Repr'), followed by one _BIT macro per bit-field.
    renderReg prefix f =
        let rname = prefix ++ "_" ++ up (fieldName f)
        in ( "#define " ++ rname ++ "  " ++ hexUL (fromIntegral (fieldOffset f) :: Word32)
                 ++ "  /* " ++ cType (fieldRepr f) (fieldWidth f) ++ " */" )
           : [ "#define " ++ rname ++ "_" ++ up (bfName b) ++ "_BIT  " ++ show (bfLoBit b)
             | b <- fieldBitFields f ]

    -- Map representation + width to a stdint C type.
    cType repr rw =
        let signed = case repr of { RSigned -> "int"; _ -> "uint" }
            bits   = case rw of { RW8 -> "8"; RW16 -> "16"; RW32 -> "32" }
        in signed ++ bits ++ "_t"

-- ---------------------------------------------------------------------------
-- sysGenLinkerScript
-- ---------------------------------------------------------------------------

-- | Generate a GNU LD linker script stub from a 'SysDoc'.
-- Emits MEMORY regions for RAM/ROM entries if present; otherwise emits
-- placeholder regions based on bus map addresses.
sysGenLinkerScript :: SysDoc -> String
sysGenLinkerScript doc = unlines $
    [ "/* Auto-generated by ISACLE */"
    , "/* Peripheral address map */"
    , "MEMORY {"
    ]
    ++ map renderPeriph (allEntries doc)
    ++
    [ "}"
    ]
  where
    hexLen w = "0x" ++ showHex w ""

    renderPeriph (_, pe) =
        "  " ++ padRight 16 (peName pe)
        ++ "(rw) : ORIGIN = " ++ hexLen (peBase pe)
        ++ ", LENGTH = " ++ hexLen (specSize (peSpec pe))
