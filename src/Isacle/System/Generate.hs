--
-- Artifact generators that consume a 'SysDoc' (or legacy 'SystemSpec').
-- None of these functions are in the synthesis path.
module Isacle.System.Generate
    ( -- * SysDoc-based generators (primary path)
      sysExtractMemoryMap
    , sysGenCHeader
    , sysGenLinkerScript
      -- * Legacy SystemSpec-based generators
    , extractMemoryMap
    , genCHeader
    , genLinkerScript
    ) where

import Prelude
import Data.Char (toUpper)
import Data.List (intercalate, sortBy)
import Data.Ord (comparing)
import Data.Word (Word8, Word32)
import Numeric (showHex)

import Isacle.System.Spec
import Isacle.System.SystemDSL  (SysDoc(..), BusSection(..), PeriphEntry(..))
